print("Olá, para calcular a área de um terreno, siga as seguintes instruções:")
comprimento = float(input("Insira o valor em metros do comprimento de seu terreno: "))
largura = float(input("Insira o valor em metros da largura de seu terreno: "))
area = comprimento * largura / 10000
print(f"Seu terreno possui uma área de {area} hectare(s).")
